package Assignment;

public class Animal {

        private String color;
        private int eyes;
        private int legs;
        private int teeths;
        void setColor(String Color)
        {
            color = Color;
        }
        void setEyes(int Eyes)
        {
            eyes = Eyes;
        }
        void setLegs(int Legs)
        {
            legs = Legs;
        }
        void setTeeths(int Teeths)
        {
            teeths = Teeths;
        }
        String getcolor()
        {
            return color;
        }
        int geteyes()
        {
            return eyes;
        }
        int getlegs()
        {
            return legs;
        }
        int getteeths()
        {
            return teeths;
        }
    }

