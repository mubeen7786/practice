public class Employee {
     int id;
     String designation;
     int Experince;

     public int getId()
     {
         return id;
     }
     public String getDesignation()
     {
         return designation;
     }
     public int getExperince()
     {
         return Experince;
     }

     public void setId( int IdValue)
     {
         id = IdValue;
     }
     public void  setDesignation(String DesignationValue)
     {
         designation = DesignationValue;
     }
     public void setExperince(int ExpValue)
     {
         Experince = ExpValue;
     }

}
